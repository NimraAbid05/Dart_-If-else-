void main() {

  // dart pogram to check whether the number is 0 or positive or negative?

  int n = -8;
  if (n == 0) {           //check n = 0
    print("Zero");
  } else if (n > 0) {    //check n is greater than 0
    print("Positive Number");
  } else if (n < 0) {   //check n is less than 0
    print("Negative Number");
  } else {               
    print("It is not a Number");
  }
}
